    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $lugar = Lugar::find($id);
        return view('lugar.edit', compact('lugar'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $edlugar = Lugar::find($id);
        $edlugar->nombre_lugar = $request->nombre_lugar;
        $edlugar->longitud = $request->longitud;
        $edlugar->latitud= $request->latitud;
        $edlugar->save();
        return redirect()->route('lugar.index')->with('success','Lugar actualizado satisfactoriamente');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Lugar $lugar)
    {
        $lugar->delete();
        return redirect()->route('lugar.index');
    }
